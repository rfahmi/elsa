const clean = (text) => {
  const specialChars = /([\\`*_{}\[\]\(\)#\+\-\.!<>$%^&?=|:";'])/g;
  const escapeMap = {
    "\\": "\\\\",
    "`": "\\`",
    "*": "\\*",
    _: "\\_",
    "{": "\\{",
    "}": "\\}",
    "[": "\\[",
    "]": "\\]",
    "(": "\\(",
    ")": "\\)",
    "#": "\\#",
    "+": "\\+",
    "-": "\\-",
    ".": "\\.",
    "!": "\\!",
    "<": "&lt;",
    ">": "&gt;",
    $: "\\$",
    "%": "\\%",
    "^": "\\^",
    "&": "&amp;",
    "=": "\\=",
    "|": "\\|",
    ":": "\\:",
    '"': '\\"',
    ";": "\\;",
  };

  return text.replace(specialChars, (match) => {
    return escapeMap[match];
  });
};

module.exports = { clean };
